public class Ex12MediaSalarios {
    public static void main(String[] args) {
        int mediaSalario;
        int n1, n2, n3, n4, n5;

        n1 = 12000;
        n2 = 13000;
        n3 = 10000;
        n4 = 5000;
        n5 = 20000;

        mediaSalario = (n1 + n2 + n3 + n4 + n5) / 5;
        
        System.out.println("A media salarial dos funcionários é de: " + "R$" + mediaSalario);

    }
}
